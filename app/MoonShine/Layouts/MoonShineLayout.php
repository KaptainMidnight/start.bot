        return [
            ...parent::assets(),
        ];
    }

    protected function menu(): array
    {
        return [
            MenuGroup::make('Система', [
                MenuItem::make('Администраторы', MoonShineUserResource::class)->icon('user-group'),
                MenuItem::make('Роли', MoonShineUserRoleResource::class)->icon('key'),
            ])->icon('circle-stack'),
